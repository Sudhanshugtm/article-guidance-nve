function normalizePeacockText(text) {
  return text.normalize('NFD').replace(/\p{M}/gu, '')
}

function escapeForRegex(word) {
  return word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

export function getPeacockWords(lang) {
  return peacockWordsByLang[lang] ?? peacockWordsByLang.en
}

export function buildPeacockPattern(words) {
  const escapedWords = words.map((word) => escapeForRegex(normalizePeacockText(word)))
  return new RegExp(`(^|[^\\p{L}\\p{N}_])(?:${escapedWords.join('|')})(?=$|[^\\p{L}\\p{N}_])`, 'iu')
}

export const peacockWords = peacockWordsByLang.en
export { normalizePeacockText }
